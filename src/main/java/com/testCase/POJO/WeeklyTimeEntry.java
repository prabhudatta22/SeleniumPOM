public class WeeklyTimeEntry {

	private int MondayHr;
	private int TuesDayHr;
	private int WednesDayHr;
	private int ThursDayHr;
	private int FridayHr;
	private int SaturdayHr;
	private int SunDayHr;
	private int total;

	public int getMondayHr() {
		return MondayHr;
	}

	public void setMondayHr(int mondayHr) {
		MondayHr = mondayHr;
	}

	public int getTuesDayHr() {
		return TuesDayHr;
	}

	public void setTuesDayHr(int tuesDayHr) {
		TuesDayHr = tuesDayHr;
	}

	public int getWednesDayHr() {
		return WednesDayHr;
	}

	public void setWednesDayHr(int wednesDayHr) {
		WednesDayHr = wednesDayHr;
	}

	public int getThursDayHr() {
		return ThursDayHr;
	}

	public void setThursDayHr(int thursDayHr) {
		ThursDayHr = thursDayHr;
	}

	public int getFridayHr() {
		return FridayHr;
	}

	public void setFridayHr(int fridayHr) {
		FridayHr = fridayHr;
	}

	public int getSaturdayHr() {
		return SaturdayHr;
	}

	public void setSaturdayHr(int saturdayHr) {
		SaturdayHr = saturdayHr;
	}

	public int getSunDayHr() {
		return SunDayHr;
	}

	public void setSunDayHr(int sunDayHr) {
		SunDayHr = sunDayHr;
	}

	public int getTotal() {
		return total;
	}

	public void setTotal(int total) {
		this.total = total;
	}

}
